    public void showEmpDetails()
    {
        TblModel.setRowCount(0);
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:MySQL://localhost:3306/hotelmgt", "root", "");
            statement = con.createStatement();
            String sql = "SELECT * FROM `employee_add`";
            result = statement.executeQuery(sql);
            
            while(result.next())
            {
                String employee_id = result.getString("empID");
                String First_name = result.getString("First Name");
                String Last_name = result.getString("Last Name");
                String Contact = result.getString("Contact");
                String Email = result.getString("Email");
                String Address = result.getString("Address");
                
                TblModel.addRow(new Object[]{employee_id,First_name,Last_name,Contact,Email,Address});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void deleteEmp(String empID)
    {
        try {
            String query = "DELETE FROM employee_add WHERE empID = " + empID;
            statement.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Employee with the ID " + empID + "is successfully deleted");
            showEmpDetails();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

