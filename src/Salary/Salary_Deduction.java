    private void calculatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculatebtnActionPerformed
        // TODO add your handling code here:
        String deduc = salDeduction.getText();
        if("".equals(deduc)){
                JOptionPane.showMessageDialog(new JFrame(),"Deduction Percentage is required","Dialog",JOptionPane.ERROR_MESSAGE);
            }
            else{
        double salary = Double.parseDouble(salary2.getText());
        double deduction = Double.parseDouble(salDeduction.getText());
        double deductionAmount = (deduction / 100) * salary;
        double netSalary = salary - deductionAmount;
        netSal.setText(String.valueOf(netSalary));
        JOptionPane.showMessageDialog(new JFrame(),"Successfully deducted salary");
        }
    }//GEN-LAST:event_calculatebtnActionPerformed

