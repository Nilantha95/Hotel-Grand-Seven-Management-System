    private void searchbtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtn2ActionPerformed
        // TODO add your handling code here:
        String ID;
        int notFound = 0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");

            String Url = "jdbc:MySQL://localhost:3306/hotelmgt";
            String User = "root";
            String Pass = "";

            Connection con = DriverManager.getConnection(Url, User, Pass);
            Statement st = con.createStatement();

            ID = searchData2.getText();

            if("".equals(ID)){
                JOptionPane.showMessageDialog(new JFrame(),"ID is required","Dialog",JOptionPane.ERROR_MESSAGE);
            }
            else{
                String sql = "SELECT * FROM addsalary WHERE id = "+ID;
                ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                    fName2.setText(rs.getString("fname"));
                    lName2.setText(rs.getString("lname"));
                    email2.setText(rs.getString("email"));
                    salary2.setText(rs.getString("salary"));
                    notFound = 1;
                }
                if(notFound == 0){
                    JOptionPane.showMessageDialog(new JFrame(),"Invalid ID","Dialog",JOptionPane.ERROR_MESSAGE);
                }
            }

        }catch(Exception e){
            System.out.println("Error "+ e.getMessage());

        }
    
    
    }//GEN-LAST:event_searchbtn2ActionPerformed

