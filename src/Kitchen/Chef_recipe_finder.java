    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String foodName = food_type.getText().trim();
        if (foodName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type a food name to search", "Empty Food Name", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
        OkHttpClient client = new OkHttpClient();
        MediaType mediaType = MediaType.parse("application/json");
        Request request = new Request.Builder()
                .url("https://api.spoonacular.com/recipes/search?apiKey=bf3302dfa4464b549f793cbab0c97d8b&query=" + foodName)
                .get()
                .build();
        Response response = client.newCall(request).execute();
        if (!response.isSuccessful()) {
            throw new RuntimeException("Failed to connect to server: " + response);
        }
        InputStream responseStream = response.body().byteStream();
        ObjectMapper mapper = new ObjectMapper();
        JsonNode root = mapper.readTree(responseStream);
        JsonNode recipes = root.get("results");
        if (recipes != null && recipes.isArray() && recipes.size() > 0) {
            JsonNode recipe = recipes.get(0);
            String recipeTitle = recipe.get("title").asText();
            String recipeUrl = recipe.get("sourceUrl").asText();
            jTextArea1.setText("Recipe: " + recipeTitle + "\n\nRecipe URL: " + recipeUrl);
        } else {
            jTextArea1.setText("Recipe not found");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error fetching recipe: " + e.getMessage(), "Recipe Fetch Error", JOptionPane.ERROR_MESSAGE);
    }

       
    }//GEN-LAST:event_jButton1ActionPerformed

