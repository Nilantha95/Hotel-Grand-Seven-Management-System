      String id;
      int notFound = 0;
      
      try{
          Class.forName("com.mysql.cj.jdbc.Driver");
          
          String Url = "jdbc:MySQL://localhost:3306/hotelmgt";
          String User = "root";
          String Pass ="";
          
          Connection con = DriverManager.getConnection(Url,User, Pass);
          Statement statement = con.createStatement();
          
          id = searchid.getText();
          
          if("".equals(id))
          {
              JOptionPane.showMessageDialog(new JFrame(),"Enter the Food Id","Dialog",JOptionPane.ERROR_MESSAGE);
          }
          else
          {
              String query = "SELECT * FROM `kitchen_food_add` WHERE id =  "+id;
              ResultSet result = statement.executeQuery(query);
              
              while(result.next())
              {
                  foodnameupdate.setText(result.getString("food_name"));
                  quantity1.setText(result.getString("quantity"));
                  suppliernameupdate.setText(result.getString("supplier_name"));
                  suppliaremail.setText(result.getString("supplier_email"));
                  supcontactnumupdate.setText(result.getString("suupplier_number"));
                  Foodid.setText(result.getString("id"));
                  notFound = 1;
              }
              if(notFound ==0)
              {
                  JOptionPane.showMessageDialog(new JFrame(),"Invalid employee ID","Dialog",JOptionPane.ERROR_MESSAGE);
              }
          }
      }catch(Exception e){
          System.out.println("Error"+e.getMessage());
      }
               
