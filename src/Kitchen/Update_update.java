    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        String ID = Foodid.getText();
        String Fname = foodnameupdate.getText();
        String quantitynew = quantity1.getText();
        String Supplier_name = suppliernameupdate.getText();
        String Email = suppliaremail.getText();
        String Supplier_number = supcontactnumupdate.getText();
        
        try{
            
          Class.forName("com.mysql.cj.jdbc.Driver");
          
          String Url = "jdbc:MySQL://localhost:3306/hotelmgt";
          String User = "root";
          String Pass ="";
          
          Connection con = DriverManager.getConnection(Url,User, Pass);
          Statement statement = con.createStatement();
          
          String query = "UPDATE  `kitchen_food_add` SET `food_name`= ? ,`quantity`= ? ,`supplier_name` = ? ,`supplier_email` = ? , `suupplier_number` = ? WHERE `id`=? ";
          PreparedStatement preparedStatment = con.prepareStatement(query);
          
          preparedStatment.setString(1,Fname );
          preparedStatment.setString(2,quantitynew );
          preparedStatment.setString(3,Supplier_name );
          preparedStatment.setString(4,Email );
          preparedStatment.setString(5,Supplier_number );
          preparedStatment.setString(6,ID );
         
          
          int rowUpdated = preparedStatment.executeUpdate();
          
          if(rowUpdated > 0)
          {
              JOptionPane.showMessageDialog(new JFrame(),"Food data updated successfully");
              loaddata();
          }else{
             JOptionPane.showMessageDialog(new JFrame(),"Fail to add food details","Dialog",JOptionPane.ERROR_MESSAGE); 
          }
          con.close();
    }//GEN-LAST:event_updatebtnActionPerformed
catch(Exception e){
    System.out.println("Error:"+e.getMessage());
}
    }

public void loaddata()
{
    try{
   Class.forName("com.mysql.cj.jdbc.Driver");
          
          String Url = "jdbc:MySQL://localhost:3306/hotelmgt";
          String User = "root";
          String Pass ="";
          
          Connection con = DriverManager.getConnection(Url,User, Pass);
          Statement statement = con.createStatement();
          
          DefaultTableModel TbModel = (DefaultTableModel)viewfood2.getModel();
          TbModel.setRowCount(0);
          String query = "SELECT * FROM `kitchen_food_add`";
          ResultSet result = statement.executeQuery(query);
          String Foodid,foodnameupdate,quantity1,suppliernameupdate,supcontactnumupdate,suppliaremail;
          
          while(result.next())
          {
              Foodid = result.getString("id");
              foodnameupdate = result.getString("food_name");
              quantity1 = result.getString("quantity");
              suppliernameupdate = result.getString("supplier_name");
              suppliaremail = result.getString("supplier_email");
              supcontactnumupdate = result.getString("suupplier_number");
              
              
              TbModel.addRow(new Object[]{Foodid,foodnameupdate,quantity1,suppliernameupdate,suppliaremail,supcontactnumupdate});
              }
          con.close();
   }catch(Exception e)
   {
       System.out.println("Error"+e.getMessage());
   }
}
