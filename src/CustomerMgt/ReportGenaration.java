   public void loadData(){
        try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        
           String url="jdbc:MySQL://localhost:3306/hotelmgt";
           String user ="root";
           String pass ="";
           
           Connection con = DriverManager.getConnection(url, user, pass);
           
           Statement st = con.createStatement();
        
       DefaultTableModel model = new DefaultTableModel(new String[]{"ID","First Name","Last Name","Address","Email Address","Contact Number"},0);
       
       reporttable.setModel(model);
       String sql = "SELECT * FROM addcustomer";
       
       ResultSet rs = st.executeQuery(sql);
       String i, f, l, a, e, c;
       while(rs.next()){
        i = rs.getString("id");
        f = rs.getString("firstname");
        l = rs.getString("lastname");
        a = rs.getString("address");
        e = rs.getString("email");
        c = rs.getString("contactnumber");
        model.addRow(new Object[]{i, f, l, a, e, c});
    }
       
    }catch(Exception e){
                System.out.println("Error"+e.getMessage());
            }
    }

    
