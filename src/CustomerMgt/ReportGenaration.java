    private void generatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generatebtnActionPerformed
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int x = j.showSaveDialog(this);
        if(x == JFileChooser.APPROVE_OPTION)
        {
            path = j.getSelectedFile().getPath();
        }

        Document doc = new Document();

        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"customerData.pdf"));
            doc.open();
            PdfPTable tbl = new PdfPTable(6);
            tbl.addCell("ID");
            tbl.addCell("First Name");
            tbl.addCell("Last Name");
            tbl.addCell("Address");
            tbl.addCell("Email Address");
            tbl.addCell("Contact Number");

            for(int i=0;i<reporttable.getRowCount();i++)
            {
                String id = reporttable.getValueAt(i, 0).toString();
                String firstname = reporttable.getValueAt(i, 1).toString();
                String lastname = reporttable.getValueAt(i, 2).toString();
                String address = reporttable.getValueAt(i, 3).toString();
                String emailaddress = reporttable.getValueAt(i, 4).toString();
                String contactnumber = reporttable.getValueAt(i, 5).toString();
                tbl.addCell(id);
                tbl.addCell(firstname);
                tbl.addCell(lastname);
                tbl.addCell(address);
                tbl.addCell(emailaddress);
                tbl.addCell(contactnumber);
            }
            doc.add(tbl);
            JOptionPane.showMessageDialog(null, "PDF is generated");
            loadData();

        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "PDF cannot be generated");
        }
        doc.close();
    }//GEN-LAST:event_generatebtnActionPerformed

